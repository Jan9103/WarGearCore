package de.jan9103.java.utils.browser;

import java.io.IOException;
import java.net.MalformedURLException;
import java.net.ProtocolException;
import java.net.URL;
import java.util.List;
import java.util.Map;

public abstract class WebConnection{
	protected final Browser b;
	protected final URL u;
	protected final String method;
	protected final boolean report;
	public boolean follow=false;
	/** {{key, value},{key2, value2}} */
	protected String[][]params;
	protected String referer=null;
	public Map<String,List<String>>responseHeaderFields;
	public int responseCode=0;
	/**
	 * -2 = pre start
	 * -1 = running
	 *  0 = ok
	 *  1 = error
	 */
	public byte status=0;
	public WebConnection(Browser browser,URL url,String m,boolean report){
		b=browser;u=url;method=m;this.report=report;}
	public WebConnection params(String[][]p){params=p;return this;}
	public WebConnection referer(String s){referer=s;return this;}
	/**
	 * same as in browser, but auto adds a referer
	 */
	public WebRequest follow(String url)throws MalformedURLException,ProtocolException{
		WebRequest r=b.get(url);
		r.referer=u.toString();
		return r;
	}
	public abstract String readLine()throws IOException;
	public abstract void disconnect();
}
