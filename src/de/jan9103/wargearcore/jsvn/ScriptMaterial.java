package de.jan9103.wargearcore.jsvn;

import org.bukkit.Material;

import de.jan9103.wargearcore.worldedit.cmds.WorldEditCmd;

public class ScriptMaterial {
	private final Material c;
	private final byte d;
	public ScriptMaterial(Material a){
		c=a; d=0;
	}

	public ScriptMaterial(byte b){
		c=null; d=b;
	}

	public ScriptMaterial(String i)throws ParseException {
		if(i.charAt(0)=='.')
			switch(i.charAt(1)){
			case 'm':
				c=null;
				try{d=Byte.parseByte(i.charAt(2)+""); break;}catch(final NumberFormatException e){throw new ParseException("Material invalid (invalid material-variable id)");}

			default: throw new ParseException("Material invalid (variable type not compatible)");
			}
		else{
			d=0; c=WorldEditCmd.parseM(i); if(c==null) throw new ParseException("Invalid Material ("+i+")");
		}
	}

	public Material get(VarSet a){
		if(c!=null) return c;

		final Material b=a.gM(d);

		return b==null?Material.AIR:b;
	}

	public Material getWE(VarSet a)throws NullPointerException {
		if(c!=null) return c;

		try{final Material b=a.gMe(d);
		    if(b==null) throw new NullPointerException();
		    return b;}catch(final ArrayIndexOutOfBoundsException e){throw new NullPointerException();}
	}
}
