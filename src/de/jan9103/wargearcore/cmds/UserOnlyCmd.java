package de.jan9103.wargearcore.cmds;

import org.bukkit.command.Command;
import org.bukkit.command.CommandExecutor;
import org.bukkit.command.CommandSender;
import org.bukkit.entity.Player;

import de.jan9103.wargearcore.LogHandler;
import de.jan9103.wargearcore.User;
import de.jan9103.wargearcore.UserManager;
import de.jan9103.wargearcore.WGC;

public abstract class UserOnlyCmd implements CommandExecutor {
	@Override public boolean onCommand(CommandSender s,Command arg1,String arg2,String[] a){
		if(!(s instanceof Player)){
			s.sendMessage("§6[WarGearCore] §ePlayer-only command.");
			return true;
		}
		try{
			exe(a,UserManager.getUser((Player)s),(Player)s);
		}catch(Exception e){
			LogHandler.handleException(e);
			s.sendMessage("§c[WGC] An error occured during the execution of the command..\n§e"+(WGC.autoBugReportLog?"The Bug has already been reported to the developer.":"It would be helpful if the Server-Admin could enable auto-Bug reports in the plugin Config in order for me to be able to fix such Bugs which might occur in the future."));
		}
		return true;
	}

	public abstract void exe(String[] a,User u,Player p)throws Exception;

	public User parseTarget(String s){
		return UserManager.parseUser(s);
	}
}
