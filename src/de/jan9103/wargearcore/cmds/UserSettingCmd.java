public class UserSettingCmd extends AsyncUserCmd {
	private final User u;
	public UserSettingCmd(User u1){
		u=u1;
	}

	@Override public Runnable get(String[] a,User u,Player p){
		return new UserSettingCmd(u);
	}

	public UserSettingCmd(){
		u=null;
	}

	@Override public void run(){
		String high=ChatColor.a(u.ct,Co.HIGH),norm=ChatColor.a(u.ct,Co.NORM);
		final InvClickMenuBuilder icb=new InvClickMenuBuilder(u).setTitle(Co.NORM,"User settings.")
		                               //general
					       .addOption(Material.OAK_SIGN,Co.NORM,"'/r' responds to last message "+high+(u.rtlr?"Recieved":"sent"))
					       .addOption(Material.HEART_OF_THE_SEA,Co.NORM,"RGB JSON (1.16+): "+high+(u.pre16?"off":"on"))
					       .addOption(Material.STRUCTURE_VOID,Co.NORM,"worldedit undo: "+high+(u.saveUndo?"on":"off"))
					       .addOption(Material.SKELETON_SKULL,Co.NORM,"chat emojis: "+high+(u.emoji?"on":"off"))
					       .addOption(Material.ITEM_FRAME,Co.NORM,"advanced Tab: "+high+(u.advancedTab?"on":"off"))
					       .addOption(Material.PAINTING,Co.NORM,"Images in chat: "+high+(u.chatImages?"show":"hide"))
					       .addOption(Material.ACACIA_DOOR,Co.NORM,"Join-Panel: "+high+joinPanelName(u.joinPanel))
					       .addOption(Material.ELYTRA,Co.NORM,"Elytra Plus: "+high+(u.elytraPlus?"on":"off"))
					       .addOption(Material.PAPER,Co.NORM,"ToDo List reminder days: "+high+(u.joinTodoInfoDays==0?"off":u.joinTodoInfoDays))
					       .addOption(Material.IRON_BARS,Co.NORM,"AuthMe mode: "+high+(u.authPwMode==0?"off":(u.authPwMode==1?"once per ip":"every login")))
					       .addOption(Material.POTTED_FERN,Co.NORM,"Alternative Block Material: "+high+(u.special_blocks?"on":"off"))
					       .ln()// theme
					       .addOption(Material.GRASS_BLOCK,ChatColor.a(ChatTheme.BUKKIT,Co.HIGH)+"Vanilla "+ChatColor.a(ChatTheme.BUKKIT,Co.NORM)+" Chat Theme")
					       .addOption(Material.HONEYCOMB,ChatColor.a(ChatTheme.SW,Co.HIGH)+"VaporWarfare "+ChatColor.a(ChatTheme.SW,Co.NORM)+" Chat Theme")
					       .addOption(Material.OCELOT_SPAWN_EGG,ChatColor.a(ChatTheme.MPP,Co.HIGH)+"YourGamingPlanet "+ChatColor.a(ChatTheme.MPP,Co.NORM)+" Chat Theme")
					       .addOption(Material.PINK_WOOL,ChatColor.a(ChatTheme.PASTELL,Co.HIGH)+"Pastell "+ChatColor.a(ChatTheme.PASTELL,Co.NORM)+" Chat Theme")
					       .addOption(Material.SNOW_BLOCK,ChatColor.a(ChatTheme.YETI,Co.HIGH)+"Yeti "+ChatColor.a(ChatTheme.YETI,Co.NORM)+" Chat Theme")
					       .addOption(Material.BLAST_FURNACE,ChatColor.a(ChatTheme.XAMPP,Co.HIGH)+"XAMPP "+ChatColor.a(ChatTheme.XAMPP,Co.NORM)+" Chat Theme")
					       .addOption(Material.CYAN_TERRACOTTA,ChatColor.a(ChatTheme.WTU,Co.HIGH)+"WTUtil "+ChatColor.a(ChatTheme.WTU,Co.NORM)+" Chat Theme")
					       .addOption(Material.BLACK_GLAZED_TERRACOTTA,ChatColor.a(ChatTheme.PHC,Co.HIGH)+"PHCore "+ChatColor.a(ChatTheme.PHC,Co.NORM)+" Chat Theme")
					       .addOption(Material.PACKED_ICE,ChatColor.a(ChatTheme.ISA,Co.HIGH)+"Interstellar "+ChatColor.a(ChatTheme.ISA,Co.NORM)+" Chat Theme")
					       .addOption(Material.GREEN_WOOL,ChatColor.a(ChatTheme.OLD,Co.HIGH)+"Old WGC "+ChatColor.a(ChatTheme.OLD,Co.NORM)+" Chat Theme")
					       .ln()// volume
					       .addOption(Material.MUSIC_DISC_11,Co.NORM,"Chat-ping volume: "+ChatColor.a(u.ct,Co.HIGH)+u.getSoundVolume(SoundKategory.CHATPING)+ChatColor.a(u.ct,Co.NORM)+"%")
					       .addOption(Material.MUSIC_DISC_13,Co.NORM,"Area-flag change volume: "+ChatColor.a(u.ct,Co.HIGH)+u.getSoundVolume(SoundKategory.AREA_STATE_CHANGE)+ChatColor.a(u.ct,Co.NORM)+"%")
					       .addOption(Material.MUSIC_DISC_BLOCKS,Co.NORM,"Command volume: "+ChatColor.a(u.ct,Co.HIGH)+u.getSoundVolume(SoundKategory.COMMANDS)+ChatColor.a(u.ct,Co.NORM)+"%")
					       .addOption(Material.MUSIC_DISC_CAT,Co.NORM,"Timer volume: "+ChatColor.a(u.ct,Co.HIGH)+u.getSoundVolume(SoundKategory.TIMER)+ChatColor.a(u.ct,Co.NORM)+"%")
					       .addOption(Material.MUSIC_DISC_CHIRP,Co.NORM,"Fight framework volume: "+ChatColor.a(u.ct,Co.HIGH)+u.getSoundVolume(SoundKategory.FIGHT_BASE)+ChatColor.a(u.ct,Co.NORM)+"%")
					       .addOption(Material.MUSIC_DISC_FAR,Co.NORM,"Fight event volume: "+ChatColor.a(u.ct,Co.HIGH)+u.getSoundVolume(SoundKategory.FIGHT_EVENT)+ChatColor.a(u.ct,Co.NORM)+"%");

		Bukkit.getScheduler().runTask(WGC.wgc,()->icb.build((e,us)->{
			if(e.getSlotType()==SlotType.OUTSIDE) return;

			if(e.getClickedInventory() instanceof PlayerInventory) return;

			e.setCancelled(true);
			final ItemStack i=e.getInventory().getItem(e.getSlot());
			if(i==null) return;

			switch(i.getType()){
			// general settings
			case OAK_SIGN: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"'/r' responds to last message "+ChatColor.a(u.ct,Co.HIGH)+((u.rtlr=!u.rtlr)?"Recieved":"sent"));
				i.setItemMeta(m);
				return;
			}

			case HEART_OF_THE_SEA: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"RGB JSON (1.16+): "+ChatColor.a(u.ct,Co.HIGH)+((u.pre16=!u.pre16)?"off":"on"));
				i.setItemMeta(m);
				return;
			}

			case STRUCTURE_VOID: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"worldedit undo: "+ChatColor.a(u.ct,Co.HIGH)+((u.saveUndo=!u.saveUndo)?"on":"off"));
				i.setItemMeta(m);
				return;
			}

			case SKELETON_SKULL: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"chat emojis: "+ChatColor.a(u.ct,Co.HIGH)+((u.emoji=!u.emoji)?"on":"off"));
				i.setItemMeta(m);
				return;
			}

			case ITEM_FRAME: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"advanced Tab: "+ChatColor.a(u.ct,Co.HIGH)+((u.advancedTab=!u.advancedTab)?"on":"off"));
				i.setItemMeta(m);
				return;
			}

			case PAINTING: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"Images in Chat: "+ChatColor.a(u.ct,Co.HIGH)+((u.advancedTab=!u.advancedTab)?"show":"hide"));
				i.setItemMeta(m);
				return;
			}                       //"Join-Panel: "+joinPanelName(u.joinPanel)

			case ELYTRA: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"Elytra Plus: "+ChatColor.a(u.ct,Co.HIGH)+((u.elytraPlus=!u.elytraPlus)?"on":"off"));
				i.setItemMeta(m);
				return;
			}

			case ACACIA_DOOR: {
				if(++u.joinPanel>2) u.joinPanel=0;
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"Join-Panel: "+ChatColor.a(u.ct,Co.HIGH)+(joinPanelName(u.joinPanel))+ChatColor.a(u.ct,Co.NORM)+".");
				i.setItemMeta(m);
				JoinListener.senJoinInfo((Player)e.getWhoClicked(),u);
				return;
			}

			case PAPER: {
				if(e.isRightClick()){
					if(--u.joinTodoInfoDays<0) u.joinTodoInfoDays=10;
				}
				else   if(++u.joinTodoInfoDays<0) u.joinTodoInfoDays=0;
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"ToDo List reminder days: "+ChatColor.a(u.ct,Co.HIGH)+(u.joinTodoInfoDays==0?"off":u.joinTodoInfoDays));
				i.setItemMeta(m);
				return;
			}

			case IRON_BARS: {
				if(++u.authPwMode>2) u.authPwMode=0;
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"AuthMe mode: "+(u.authPwMode==0?"off":(u.authPwMode==1?"once per ip":"every login")));
				i.setItemMeta(m);
				return;
			}

			case POTTED_FERN: {
				final ItemMeta m=i.getItemMeta();
				m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+"Alternative Block Material: "+ChatColor.a(u.ct,Co.HIGH)+((u.special_blocks=!u.special_blocks)?"on":"off")+ChatColor.a(u.ct,Co.NORM)+".");
				i.setItemMeta(m);
				return;
			}

			// chat theme
			case BLACK_GLAZED_TERRACOTTA: u.ct=ChatTheme.PHC; a("PHCore"); return;

			case CYAN_TERRACOTTA: u.ct=ChatTheme.WTU; a("WTUtil"); return;

			case GREEN_WOOL: u.ct=ChatTheme.OLD; a("WGC-Old"); return;

			case PINK_WOOL: u.ct=ChatTheme.PASTELL; a("Pastell"); return;

			case GRASS_BLOCK: u.ct=ChatTheme.BUKKIT; a("Vanilla"); return;

			case OCELOT_SPAWN_EGG: u.ct=ChatTheme.MPP; a("YourGamingPlanet"); return;

			case HONEYCOMB: u.ct=ChatTheme.SW; a("VaporWarfare"); return;

			case PACKED_ICE: u.ct=ChatTheme.ISA; a("Interstellar"); return;

			case SNOW_BLOCK: u.ct=ChatTheme.YETI; a("Yeti"); return;

			case BLAST_FURNACE: u.ct=ChatTheme.XAMPP; a("XAMPP"); return;

			// volume
			case MUSIC_DISC_11: vol(SoundKategory.CHATPING,"Chat-ping",i,e.isLeftClick(),e.isShiftClick(),Sound.BLOCK_BREWING_STAND_BREW,2); return;

			case MUSIC_DISC_13: vol(SoundKategory.AREA_STATE_CHANGE,"Area-flag change",i,e.isLeftClick(),e.isShiftClick(),Sound.BLOCK_FIRE_EXTINGUISH,0.5f); return;

			case MUSIC_DISC_BLOCKS: vol(SoundKategory.COMMANDS,"Command",i,e.isLeftClick(),e.isShiftClick(),Sound.BLOCK_FIRE_EXTINGUISH,2); return;

			case MUSIC_DISC_CAT: vol(SoundKategory.TIMER,"Timer",i,e.isLeftClick(),e.isShiftClick(),Sound.BLOCK_FIRE_EXTINGUISH,2); return;

			case MUSIC_DISC_CHIRP: vol(SoundKategory.FIGHT_BASE,"Fight framework",i,e.isLeftClick(),e.isShiftClick(),Sound.BLOCK_FIRE_EXTINGUISH,2); return;

			case MUSIC_DISC_FAR: vol(SoundKategory.FIGHT_EVENT,"Fight event",i,e.isLeftClick(),e.isShiftClick(),Sound.BLOCK_FIRE_EXTINGUISH,2); return;

			default:
			}
		}));
	}

	public static String joinPanelName(int id){
		switch(id){
		case 1: return "Simplistic";

		case 2: return "Icon";

		default: return "off";
		}
	}

	/**
	 * preveiw a chat theme
	 */
	private void a(String a){
		new Msg(u).a("ChatTheme: ").c(a).d().a("Default; ").b("Wrong Input; ").c("Highlight; ").e("Error").a(u);
	}

	private void vol(SoundKategory k,String nm,ItemStack i,boolean lclick,boolean shift,Sound s,float pitch){
		final ItemMeta m=i.getItemMeta();

		if(shift){
			m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+nm+" volume: "+ChatColor.a(u.ct,Co.HIGH)+(lclick?"100":"0")+ChatColor.a(u.ct,Co.NORM)+"%"); u.setSoundVolume(k,lclick?(byte)100:0);
		}
		else m.setDisplayName(ChatColor.a(u.ct,Co.NORM)+nm+" volume: "+ChatColor.a(u.ct,Co.HIGH)+u.cycleVolume(k,(byte)25,lclick)+ChatColor.a(u.ct,Co.NORM)+"%");
		i.setItemMeta(m);
		u.playSound(k,s,pitch);
	}
}
