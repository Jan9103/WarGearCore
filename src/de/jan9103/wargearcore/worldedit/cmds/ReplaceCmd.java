package de.jan9103.wargearcore.worldedit.cmds;

import org.bukkit.Material;
import org.bukkit.entity.Player;

import de.jan9103.wargearcore.User;
import de.jan9103.wargearcore.WGC;
import de.jan9103.wargearcore.chat.Msg;
import de.jan9103.wargearcore.worldedit.WEArea;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncEditor3;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepA;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepAtA;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepAtO;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepO;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepOtA;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepOtO;

public class ReplaceCmd extends PlaceCommand {
	@Override public void execu(WEArea a,String[] args,User u,Player p){
		final Material[] to=parseIn(args[0]);

		if(to==null){
			new Msg(u,WGC._PREFIX_2_Edit).b("Unknown material.").a(p); return;
		}
		boolean upd=false;
		boolean ovr=false;

		if(args.length>1){
			for(int i=1; i<args.length; i++){
				if(args[i]=="+u")
					upd=true;
				if(args[i]=="-u")
					upd=false;
			}
			switch(args[1].toLowerCase().charAt(0)){
			case '-':
			case '+': break;

			default: ovr=true;
			}
		}
		AsyncEditor3 aso;

		if(ovr){
			final Material[] from=parseIn(args[1]);
			if(from==null){
				new Msg(u,WGC._PREFIX_2_Edit).b("Unknown material.").a(p); return;
			}
			if(to.length==1){
				if(from.length==1) aso=new AsyncRepOtO(a.w,a.toBigArea(),to[0],from[0],upd,u);
				else aso=new AsyncRepOtA(a.w,a.toBigArea(),to[0],from,upd,u);
			}
			else{
				if(from.length==1) aso=new AsyncRepAtO(a.w,a.toBigArea(),to,from[0],upd,u);
				else aso=new AsyncRepAtA(a.w,a.toBigArea(),to,from,upd,u);
			}
		}
		else if(to.length==1) aso=new AsyncRepO(a.w,a.toBigArea(),to[0],upd,u);
		else aso=new AsyncRepA(a.w,a.toBigArea(),to,upd,u);
		if(u.saveUndo) u.saveUndo(aso.getUndo(),"rep-"+to[0].toString());
		aso.aFromSync();
	}
}
