package de.jan9103.wargearcore.worldedit.cmds;

import org.bukkit.Material;
import org.bukkit.entity.Player;

import de.jan9103.wargearcore.User;
import de.jan9103.wargearcore.WGC;
import de.jan9103.wargearcore.chat.Msg;
import de.jan9103.wargearcore.coordSys.BigArea;
import de.jan9103.wargearcore.coordSys.BigCoord;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncEditor3;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepA;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepAtA;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepAtO;
import de.jan9103.wargearcore.worldedit.editors.async.AsyncRepO;

public class RepnearCmd extends WorldEditCmd {
	@Override
	public void exec(String[] args,User u,Player p){
		final int     rng=Integer.parseInt(args[0]);
		final BigArea a  =new BigArea(new BigCoord(p.getLocation().getBlockX()-rng,p.getLocation().getBlockY()-rng,p.getLocation().getBlockZ()-rng),new BigCoord(p.getLocation().getBlockX()+rng,p.getLocation().getBlockY()+rng,p.getLocation().getBlockZ()+rng));

		if(rng<1) return; //TODO error

		final Material[] to=parseIn(args[1]);

		if(to==null){
			new Msg(u,WGC._PREFIX_2_Edit).b("Unknown material.").a(p);
			return;
		}
		boolean      upd=false;
		boolean      ovr=false;
		AsyncEditor3 aso;

		if(args.length>2){
			for(int i=1; i<args.length; i++){
				if(args[i]=="+u")
					upd=true;
				if(args[i]=="-u")
					upd=false;
			}
			switch(args[2].toLowerCase().charAt(0)){
			case '-':
			case '+': break;

			default: ovr=true;
			}
		}
		if(ovr){
			final Material[] from=parseIn(args[2]);
			if(from==null){
				new Msg(u,WGC._PREFIX_2_Edit).b("Unknown material.").a(p); return;
			}
			if(to.length==1) aso=new AsyncRepAtO(p.getWorld(),a,from,to[0],upd,u);
			else aso=new AsyncRepAtA(p.getWorld(),a,from,to,upd,u);
		}
		else if(to.length==1) aso=new AsyncRepO(p.getWorld(),a,to[0],upd,u);
		else aso=new AsyncRepA(p.getWorld(),a,to,upd,u);

		if(u.saveUndo) u.saveUndo(aso.getUndo(),"repnear-"+to[0].toString());
		aso.aFromSync();
	}
}
