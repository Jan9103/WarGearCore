package de.jan9103.wargearcore.extension.vars;

public class LiveCVarLink extends CVar {
	public final String name;
	public CVar var;
	public final boolean global;
	public LiveCVarLink(String name,CVar c,boolean global){
		this.name=name; this.global=global; var=c;
	}

	@Override public CVar add(CVar v){
		CVar i=var.add(v); return i==var?this:i;
	}

	@Override public CVar sub(CVar v){
		CVar i=var.sub(v); return i==var?this:i;
	}

	@Override public CVar mul(CVar v){
		CVar i=var.mul(v); return i==var?this:i;
	}

	@Override public CVar div(CVar v){
		CVar i=var.div(v); return i==var?this:i;
	}

	@Override public CVar mod(CVar v){
		CVar i=var.mod(v); return i==var?this:i;
	}

	@Override public CVar addEq(CVar v){
		CVar i=var.addEq(v); return i==var?this:i;
	}

	@Override public CVar subEq(CVar v){
		CVar i=var.subEq(v); return i==var?this:i;
	}

	@Override public CVar mulEq(CVar v){
		CVar i=var.mulEq(v); return i==var?this:i;
	}

	@Override public CVar divEq(CVar v){
		CVar i=var.divEq(v); return i==var?this:i;
	}

	@Override public CVar modEq(CVar v){
		CVar i=var.modEq(v); return i==var?this:i;
	}

	@Override public String toString(){
		return var.toString();
	}

	@Override public int toInt(){
		return var.toInt();
	}

	@Override public boolean equals(CVar v){
		return v.equals(v);
	}

	@Override public CVar clone(){
		return var.clone();
	}

	@Override public String debug(){
		return "LineCVarLink: "+name;
	}

	@Override public CVar run(String cmd,ArrVar a){
		return this;
	}
}
