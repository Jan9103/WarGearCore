package de.jan9103.wargearcore.internal_test;

import java.util.ArrayList;

public class WgcInternalTests {
	public static boolean DEBUG=false;

	public static void main(String[] a){
		DEBUG=true;
		new WgcInternalTests();
	}

	public String output="NOT YET FINISHED..";
	public int fails=0,success=0;

	public WgcInternalTests(){
		if(DEBUG) System.out.println(Test.headerLine("STARTING TESTS",'#'));
		String         o="";
		ArrayList<Test>t=new ArrayList<>();

		t.add(new FileSystemTests());
		t.add(new ConfigTest());
		//t.add(new EncryptionTests());
		//TODO add more tests
		for(Test i:t) i.start();
		for(Test i:t){
			try{i.join();}catch(InterruptedException e){}
			if(DEBUG) System.out.println(i.getMsg());
			fails  +=i.test_fail;
			success+=i.test_success;
			o      +=i.getMsg();
		}
		output=o;
		if(DEBUG) System.out.println("\n"+Test.headerLine("TESTS FINISHED",'#'));
	}
}
