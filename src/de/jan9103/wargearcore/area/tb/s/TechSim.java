public class TechSim extends Shield {
	@Override public void b(WgArea a,WeSender s){
		// TODO Auto-generated method stub
	}

	@Override public void aLater(AsyncMultiEditor ame,WgArea a){
		// TODO Auto-generated method stub
		Random  r=new Random();
		int     i=a.redWg.size();
		BigArea b=a.redWg;

		Material[] mat =new Material[] {ATheme.get(a.thm,ThemeMat.ENEMY_CONCRETE_DARK,false),ATheme.get(a.thm,ThemeMat.ENEMY_CONCRETE_LIGHT,false)};
		Material[] tech=new Material[] {ATheme.get(a.thm,ThemeMat.ENEMY_SLAB,false),ATheme.get(a.thm,ThemeMat.ENEMY_TERRA_DARK,false),Material.REDSTONE_BLOCK,Material.END_STONE};
		if(i<10000){
			a.bc(new BcMsg(WGC._PREFIX_2_Area).b("Techsimulation is not yet available for micro."));
			return; //uhmm
		}
		if(i<13000){    //PP T1 --> 6 (12)
			a.bc(new BcMsg(WGC._PREFIX_2_Area).b("Techsimulation is not yet available for t1."));
			return;
		}
		if(i<22000){//SW MWG --> 8+1 (8/2)
			new MWGTechSim(mat,tech,b,r,ame,a).gen();
			return;
		}
		if(i<51000){//PP T2 --> 10 (28)
			a.bc(new BcMsg(WGC._PREFIX_2_Area).b("Techsimulation is not yet available for t2."));
			return;
		}
		//PP / SW T3 --> PP:16 SW:14
		if(a.ausfahrm>=20){
			//SW WG
		}
		else
			//PP T3
			new PPT3TechSim(mat,tech,b,r,ame,a).gen();
		if(a.ausfahrm<20) //mpp t3 --> +2 kanonen
			a.bc(new BcMsg(WGC._PREFIX_2_Area).b("Techsimulation is not yet available for t3 - using sw-wg techSimulation instead."));
	}

	@Override public String toString(){
		return "tech";
	}
}
