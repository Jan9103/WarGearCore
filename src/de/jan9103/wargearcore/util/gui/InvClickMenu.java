package de.jan9103.wargearcore.util.gui;

import java.util.ArrayList;
import java.util.function.BiConsumer;

import org.bukkit.Bukkit;
import org.bukkit.Material;
import org.bukkit.entity.Player;
import org.bukkit.event.inventory.InventoryClickEvent;
import org.bukkit.inventory.Inventory;
import org.bukkit.inventory.ItemStack;
import org.bukkit.inventory.meta.ItemMeta;

import de.jan9103.wargearcore.User;
import de.jan9103.wargearcore.UserManager;
import de.jan9103.wargearcore.chat.ChatColor;
import de.jan9103.wargearcore.chat.Co;

public class InvClickMenu extends UserGui {
	public InvClickMenu(Inventory i,User us,BiConsumer<InventoryClickEvent,User>h){
		super(h,us);
		us.getPlayer().openInventory(i);
		inv.put(us.getPlayer(),this);
	}

	public static class InvClickMenuBuilder {
		private final ArrayList<ItemStack>itms=new ArrayList<>();
		private final User u;
		private String title="";
		public InvClickMenuBuilder(Player target){
			u=UserManager.getUser(target);
		}

		public InvClickMenuBuilder setTitle(String s){
			title=s; return this;
		}

		public InvClickMenuBuilder setTitle(Co c,String s){
			title=ChatColor.a(u.ct,c)+s; return this;
		}

		public InvClickMenuBuilder(User user){
			u=user;
		}

		public InvClickMenuBuilder addOption(Material m,Co c,String name){
			return addOption(m,ChatColor.a(u.ct,c)+name);
		}

		public InvClickMenuBuilder fill(int count){
			final ItemStack is=new ItemStack(Material.GRAY_STAINED_GLASS_PANE);
			final ItemMeta  m =is.getItemMeta();

			m.setDisplayName(" ");
			is.setItemMeta(m);
			for(int i=0; i<count; i++) itms.add(is);
			return this;
		}

		public InvClickMenuBuilder ln(){
			final int i=9-itms.size()%9; return i==9?this:fill(i);
		}

		public InvClickMenuBuilder addOption(Material m,String name){
			if(u==null) return this;

			final ItemStack i   =new ItemStack(m);
			final ItemMeta  meta=i.getItemMeta();

			meta.setDisplayName(name);
			i.setItemMeta(meta);
			itms.add(i);
			return this;
		}

		public void build(BiConsumer<InventoryClickEvent,User>handler){
			ln();
			int size=itms.size();

			if(size<1) return;

			final Inventory   i  =Bukkit.createInventory(null,size,title);
			final ItemStack[] isa=new ItemStack[size--];

			for(; size>=0; size--) isa[size]=itms.get(size);
			i.setContents(isa);
			new InvClickMenu(i,u,handler);
		}

		public int options(){
			return itms.size();
		}
	}
}
